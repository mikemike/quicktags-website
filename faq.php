          <div class="faq">
            <a href="#what-are-quicktags" name="what-are-quicktags"><h5>What are QuickTags?</h5></a>
            <div class="entry">
              <p>
                QuickTags are created using NFC (Near-Field Communication), which is a technology
                that has been around for a while.  Small strips of metal are hidden within stickers,
                posters, key rings, or anything you want.  These metal strips are programmed with a 
                specific task (such as turning on or off bluetooth) which then communicate that with
                an NFC enabled device.
              </p>
            </div>
          </div> <!-- .faq -->

          <div class="faq">
            <a href="#what-can-quicktags-do" name="what-can-quicktags-do"><h5>What can QuickTags do?</h5></a>
            <div class="entry">
              <p>
                QuickTags can do anything your device can do.  Unlike other NFC tags which are available,
                ours are different.  QuickTags come with a free app which is installed to your device
                the first time you scan a QuickTag.  Once the app is installed the QuickTags communicate
                directly with the app, allowing QuickTags to do anything the device is capable of doing.
              </p>
              <p>
                Some examples of uses include:
              </p>
              <ul>
                <li>Turn Bluetooth on and off</li>
                <li>Connect to a WiFi network</li>
                <li>Download a PDF menu or brochure</li>
                <li>Visit a website</li>
                <li>Turn the device off</li>
                <li>Install/launch an app</li>
              </ul>
            </div>
          </div> <!-- .faq -->

